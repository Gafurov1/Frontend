// Дела должны быть отделены запятой (помыть голову,сделать дз,купить еду коту). После того как пользователь закончил ввод,
// нужно в окне браузере отобразить данный список при помощи тэга <ul>. Также необходимо в каждом пункте отобразить время,
// когда именно запланировано дело. Время устанавливаться автоматически, т.е. первое дело планируем на 7:00, второе на 8:00.
